package ip.industrialProcessing.gui3.framework;

public class Thickness {
    public Thickness(float top, float right, float bottom, float left) {
	this.top = top;
	this.left = left;
	this.bottom = bottom;
	this.right = right;
    }
    public float top;
    public float left;
    public float right;
    public float bottom;
}
